                      <p><strong>PERATURAN TRANSAKSI ON-LINE TRADING (KETENTUAN UMUM)</strong></p>
                      <ol type="a">
                          <li>
                              Overnight Trading<br>
                              Adalah cara bertransaksi dengan membiarkan posisi tetap terbuka pada rekening Nasabah ketika Pasar ditutup.
                          </li>

                          <li>
                              Day Trading<br>
                              Adalah transaksi jual dan beli yang dilakukan pada hari yang sama, sehingga tidak ada posisi terbuka baru pada rekening Nasabah ketika Pasar ditutup. Hal ini juga dikenal sebagai Intraday Trading.
                          </li>

                          <li>
                              Likuidasi<br>
                              Adalah tindakan yang dilakukan untuk menutupi atau menghapus posisi terbuka dengan cara melakukan transaksi yang berlawanan atas jumlah lot dan kontrak yang sama dengan posisi yang dimiliki semula, sebelum kontrak jatuh tempo.
                          </li>

                          <li>
                              Posisi Terbuka (Open Position)<br>Adalah posisi beli (long) atau posisijual (short) yang belum dilikuidasi.
                          </li>

                          <li>
                              Harga Penyelesaian (Settlement Price)<br>Adalah harga yang ditentukan Bursa sebagai harga resmi pada akhir hari perdagangan sesuai dengan spesifikasi kontrak masing-masing.
                          </li>

                          <li>
                              Spread<br>
                              Adalah selisih dalam poin antara harga sedia jual (bid) dan harga sedia beli (ask).
                          </li>

                          <li>
                              Locking<br>
                              Pembukaan posisi baru yang berlawanan dengan posisi sebelumnya dan pada kontrak yang sama tanpa bermaksud melikuidasi.
                          </li>

                          <li>
                              Poin (Point)<br>
                              Adalah kesatuan terkecil antara suatu harga sebelumnya yang dapat dinyatakan dalam satuan angka penuh atau satuan angka sekian decimal dibelakang koma tergantung pada kebiasaan masing-masing produk.
                          </li>

                          <li>
                              Keadaan Hectic Market<br>
                              Adalah keadaan ketika pasar dalam kondisi tidak diharapkan. Pada situasi ini spread akan didasarkan
                              pada kondisi pergerakan harga sebagaimana quotasi yang disampaikanoleh provider. Yang dengan kondisi hectic apabila dipenuhi minimal salah satu dari situasi sebagai berikut.
                              <ol type="a">
                                  <li>Bid atau ask hanya ada satu</li>
                                  <li>Terjadi fluktuasi harga lebih dari 30 poin</li>
                              </ol>
                          </li>

                          <li>
                              Market Order
                              <ul>
                                  <li>
                                      Instan Excecution<br>
                                      Adalah amanat dari nasabah untuk mengambil harga sedia jual (bid) atau harga sedia beli (ask) yang
                                      pada saat itu.
                                  </li>
                                  <li>
                                      Market Excecution<br>
                                      Adalah amanat dari nasabah untuk mengambil harga sedia jual (bid) atau harga sedia beli (ask) yang pada saat itu, dimana harganya dapat berada diatas atau dibawah atau sama dengan harga yang diminta.
                                  </li>
                              </ul>
                          </li>

                          <li>
                              Limit Order<br>
                              Adalah amanat dari Nasabah untuk mengambil harga sedia jual (bid) atau harga sedia beli (ask) pada saat mencapai suatu harga tertentu. Biasanya digunakan untuk membuka posisi atau melikuidasi.
                          </li>

                          <li>
                              Stop Order<br>
                              Adalah amanat dari Nasabah untuk mengambil harga sedia jual (bid) atau harga sedia beli (ask) kalau sudah mencapai suatu harga tertentu atau lebih buruk. Biasanya digunakan untuk menutup posisi agar tidak menderita rugi lebih besar.
                          </li>

                          <li>
                              Larangan Split Order<br>
                              Larangan adanya pemecahan (split) order untuk setiap 1 (satu) kali klik transaksi yang dilakukan oleh nasabah.
                          </li>

                          <li>
                              Larangan Reject<br>
                              Adanya larangan reject oleh penyelenggara Sistem Perdagangan Alternatif atas order nasabah selain reject atas order yang melebihi maksimum lot yang diperkenankan.
                          </li>

                          <li>
                              Informasi Jeda Waktu<br>
                              Jeda waktu (delay) yang dimaksud adalah paling lama 4 detik yang mungkin terjadi disistem perdagangan dalam pengolahan order nasabah sejak order diterima sampai dengan order direspon oleh sistem perdagangan penyelenggara Sistem Perdagangan Alternatif (SPA), dalam hal metode eksekusi transaksi yang dipilih nasabah adalah Instan Eksekusi.
                          </li>

                          </ol>
