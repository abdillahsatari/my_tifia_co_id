                                                  <li>Stop Order yaitu amanat membuka posisi terbuka Nasabah Peserta pada harga tertentu, dengan tujuan untuk meminimalkan kerugian yang timbul. Stop Order otomatis menjadi Market Order pada saat harga yang ditetapkan tersentuh/terlampaui (lebih buruk).</li>
                                                  <li>Stop Loss Order yaitu amanat yang menutupi posisi terbuka Nasabah pada harga tertentu, dengan tujuan untuk meminimalkan kerugian yang timbul. Stop Loss Order otomatis menjadi Market Order pada saat harga yang ditetapkan tersentuh/terlampaui (lebih buruk). Untuk Kontrak Derivatif Antar Mata Uang, Kontrak Gulir Emas harga dieksekusi pada "order price" dan Kontrak Derivatif Indeks Saham harga akan dieksekusi pada harga " Market Price".</li>
                                                  <li>Limit Order yaitu amanat untuk membuka atau menutup posisi terbuka Nasabah pada harga tertentu. Limit Order menjadi Market Order pada saat harga yang ditetapkan tersentuh/terlampaui (lebih baik). Untuk Kontrak Derivatif Antar Mata Uang, Kontrak Gulir Emas harga dieksekusi pada "order price" dan Kontrak Derivatif Indeks Saham harga akan dieksekusi pada harga Market Price".</li>
                                                  <li>Take Profit yaitu amanat untuk menutup posisi terbuka Nasabah Peserta pada harga tertentu, dengan tujuan untuk meminimalkan kerugian yang timbul. Teka Profit otomatis menjadi Market Order pada saat harga yang ditetapkan tersentuh/terlampaui (lebih baik).</li>
