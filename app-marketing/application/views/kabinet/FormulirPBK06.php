                                      <ol>

                                          <li>
                                              Saldo Awal<br>
                                              Adalah sejumlah dana minimum yang disetor nasabah ke Rekening Terpisah yang ditunjuk oleh PT Tifia Finansial Berjangka pada awal pembukaan rekening yang besarnya minimum USD 500 (Lima Ratus US Dollar) atau equivalent untuk Rekening Transaksi Kontrak derivative antar Mata Uang, Kontrak Komoditi (Loco London, Silver dan Crude Oil).
                                          </li>

                                          <li>
                                              Saldo Baru<br>
                                              Adalah sejumlah dana yang berada pada rekening Nasabah setelah saldo sebelumnya ditambah setoran margin baru, keuntungan dan pendapatan bunga, dikurangi dengan penarikan dana, kerugian, beban bunga, komisi dan beban keuangan lainnya.
                                          </li>

                                          <li>
                                              Equity<br>
                                              Adalah saldo baru ditambah/dikurangi keuntungan/kerugian yang belum terealisasi.
                                          </li>

                                          <li>
                                              Laba/Rugi Yang Belum Terealisasi (Floating Profit/Loss)<br>
                                              Adalah keuntungan atau kerugian yang belum terealisasi pada posisi terbuka. Keuntungan dan kerugian yang belum terealisasi ini meningkat atau berkurang sesuai dengan situasi pasar dan akan direalisasi ketika posisi terbuka ini ditutup. Keuntungan dan kerugian yang belum terealisasi untuk posisi terbuka dihitung berdasarkan pada harga sediajual (bid) dan sedia beli (ask).
                                          </li>

                                          <li>
                                              Komisi (Commission)<br>
                                              Adalah sejumlah dana yang dikenakan kepada Nasabah untuk setiap lot transaksi.
                                          </li>

                                          <li>
                                              Biaya Bunga dan Beban Keuangan Lainnya (Swap)<br>
                                              Adalah biaya bunga dan beban keuangan lainnya yang dikenakan kepada Nasabah terhadap setiap posisi terbuka dari kontrak yang diperdagangkan dan dapat berubah dari waktu ke waktu.
                                          </li>

                                          <li>
                                              Margin<br>
                                              Adalah Jumlah kecukupan Margin yang dibutuhkan untuk mengambil posisi. Perhatikan adanya perbedaan antara Margin Hedging dan Margin Overnight.
                                          </li>

                                          <li>
                                              Margin Level (Margin Call Level)<br>
                                              Adalah perbandingan antara Equity dengan Margin (dalam persen) Margin Level = (Equity/Margin) x 100%.
                                          </li>

                                          <li>
                                              Free Margin/Available Margin<br>
                                              Adalah selisih antara Equity dengan Margin<br>
                                              Equity Margin - Necessary Margin
                                          </li>

                                      </ol>

