    <p><strong>I. KETENTUAN UMUM</strong></p>

    <ol>

        <li>
            <strong>Overnight Trading</strong>
            <br>
            Adalah cara bertransaksi dengan membiarkan posisi tetap terbuka pada rekening Nasabah ketika Pasar Spot/Cash/Futures/OTC di tutup.
        </li>

        <li>
            <strong>Day Trading</strong>
            <br>
            Adalah transaksi jual dan beli yang dilakukan pada hari yang sama, sehingga tidak ada posisi terbuka baru pada rekening Nasabah ketika Pasar Spot/Cash/Futures/OTC ditutup. Hal ini dikenal sebagai Intraday Trading.
        </li>

        <li>
            <strong>Likuidasi</strong>
            <br>
            Adalah tindakan yang dilakukan untuk menutup atau menghapus posisi terbuka dengan cara melakukan transaksi sejumlah posisi yang sama pada posisi yang berlawanan dengan posisi yang dimiliki semula, sebelum kontrak jatuh tempo.
        </li>

        <li>
            <strong>Posisi Terbuka (Open Position)</strong>
            <br>
            Adalah posisi beli (long) atau posisi jual (short) yang belum dilikuidasi.
        </li>

        <li>
            <strong>Kewajiban Margin (Margin Requirements)</strong>
            <br>
            Adalah harga yang ditentukan Bursa sebagai harga resmi pada akhir hari perdagangan sesuai dengan spesifikasi kontrak masing-masing.
        </li>

        <li>
            <strong>Harga Penyelesaian (Settlement Price)</strong>
            <br>
            Adalah harga yang ditentukan oleh Bursa Berjangka Jakarta sebagai harga resmi pada akhir hari perdagangan sesuai dengan spesifikasi kontrak masing-masing.
        </li>

        <li>
            <strong>Spread</strong>
            <br>
            Adalah selisih dalam poin antara harga sedia beli (bid) dan harga sedia jual (offer)
        </li>

        <li>
            <strong>Poin (point)</strong>
            <br>
            Adalah satuan terkecil antara suatu harga dengan harga sebelumnya yang dapat dinyatakan dalam satuan angka penuh atau satuan angka sekian desimal dibelakang koma tergantung pada kebiasaan masingmasing kontrak. Dalam perdagangan kontrak antar valuta (forex) ini biasa disebut sebagai satu 'PIP'.
        </li>

        <li>
            <strong>Keadaan Hectic Market</strong>
            <br>
            Adalah keadaan ketika pasar dalam kondisi yang tidak normal atau tidak menentu. Pada situasi ini spread akan didasarkan pada kondisi pergerakan harga sebagaimana quotasi yang disampaikan oleh provider yang digunakan oleh Pedagang Penyelenggara.
            <br>
            Yang dimaksud dengan kondisi hectic apabila dipenuhi minimal salah satu dari situasi sebagai berikut :

            <ol type="a">
                <li>bid atau offer hanya ada satu sisi;</li>
                <li>spread antara bid dan offer lebih dari spread normal yang ditetapkan oleh Pedagang Penyelenggara; atau</li>
                <li>adanya berita politik, ekonomi, terorisme, bencana alam dan hal-hal lain yang mempengaruhi kondisi
                    pasar financial.
                    <br>
                    Kondisi a, b dan c terjadi bukan disebabkan wrong quote
                </li>
            </ol>
        </li>

        <li>
            <strong>Kesalahan Kuotasi (Wrong Quote)</strong>
            <br>
            Adalah suatu keadaan dimana harga sedia jual (bid) atau harga sedia beli (ask) yang ditampilkan pada MetaTrader 4 Client Terminal tidak mencerminkan harga sedia jual (bid) atau harga sedia beli (ask) /keadaan pasar yang sebenarnya.
            <br>
            Suatu kuotasi dianggap salah apabila:

            <ol type="a">
                <li>Dalam keadaan normal, harga sedia beli (ask) lebih tinggi dari harga sedia jual (bid) running price ditambah spread, atau harga sedia jual (bid) lebih rendah dari harga sedia beli (ask) running price dikurangi spread.</li>
                <li>Harga bergerak melebihi atau sama dengan 200 poin dari harga terakhir dan kembali lagi ke harga awal sebelumnya.</li>
                <li>Harga yang terjadi dalam transaksi tidak sesuai dengan harga pasar pada saat itu dengan mengacu pada sumber harga yang ditetapkan oleh Pedagang Penyelenggara SPA.</li>
            </ol>
        </li>

        <li>
            <strong>Market Order</strong>
            <br>
            Adalah amanat dari nasabah untuk mengambil harga sedia beli (bid) atau harga sedia jual (offer) yang ada pada saat itu.
        </li>

        <li>
            <strong>Limit Order</strong>
            <br>
            Adalah amanat dari Nasabah untuk mengambil harga sedia jual (bid) atau harga sedia beli (ask) pada saat mencapai suatu harga tertentu. Biasanya digunakan untuk membuka posisi atau melikuidasi.
        </li>

        <li>
            <strong>Stop Order</strong>
            <br>
            Adalah amanat dari nasabah untuk mengambil harga sedia beli (bid) atau harga sedia jual (offer) kalau sudah mencapai suatu harga tertentu atau lebih buruk. Biasanya digunakan untuk menutup posisi agar tidak menderita rugi lebih besar lagi.
        </li>

        <li>
            <strong>Locking</strong>
            <br>
            Adalah pembukaan posisi baru yang berlawanan dengan posisi sebelumnya tanpa bermaksud untuk melikuidasi posisi.
        </li>

        <li>
            <strong>Informasi Jeda Waktu</strong>
            <br>
            Delay waktu yang mungkin terjadi di sistem Perdagangan dalam pengolahan amanat Nasabah paling lama 1 detik sejak amanat Nasabah diterima dan direspon oleh Sistem Perdagangan Alternatif, dalam hal metode eksekusi transaksi dipilih Nasabah adalah Market Execution.
        </li>

        <li>
            <strong>Split Order</strong>
            <br>
            Transaksi split order dalam 1 kali klik (transaksi) dilarang.
        </li>

        <li>
            <strong>Reject Order</strong>
            <br>
            Reject order dilarang kecuali karena alasan melebihi maximal lot order.
        </li>

    </ol>

