    <p><strong>II. SALDO REKENING</strong></p>

    <ol>

        <li>
            <strong>Saldo Awal</strong>
            <br>
            Adalah sejumlah dana minimum yang disetor nasabah ke Rekening Terpisah yang ditunjuk oleh PT
            PT Tifia Finansial Berjangka pada awal pembukaan rekening yang besarnya minimum USD 200,- (Dua Ratus US Dollar) atau equivalent untuk Rekening Transaksi Kontrak derivative antar Mata Uang, Kontrak Komoditi (Loco London, Silver dan Crude Oil).
        </li>

        <li>
            <strong>Saldo Baru</strong>
            <br>
            Adalah sejumlah dana yang berada pada rekening Nasabah setelah saldo sebelumnya ditambah setoran margin baru, keuntungan dan pendapatan bunga, dikurangi dengan penarikan dana, kerugian, beban bunga, komisi dan beban keuangan lainnya.
        </li>

        <li>
            <strong>Equity</strong>
            <br>
            Adalah saldo baru ditambah/dikurangi keuntungan/kerugian yang belum terealisasi.
        </li>

        <li>
            <strong>Laba/Rugi Yang Belum Terealisasi (Floating Profit/Loss)</strong>
            <br>
            Adalah keuntungan atau kerugian yang belum terealisasi pada posisi terbuka. Keuntungan dan kerugian yang belum terealisasi ini meningkat atau berkurang sesuai dengan situasi pasar dan akan direalisasi ketika posisi terbuka ini ditutup. Keuntungan dan kerugian yang belum terealisasi untuk posisi terbuka dihitung berdasarkan pada harga sedia jual (bid) dan sedia beli (ask).
        </li>

        <li>
            <strong>Komisi (Commission)</strong>
            <br>
            Adalah sejumlah dana yang dikenakan kepada Nasabah untuk setiap lot transaksi.
        </li>

        <li>
            <strong>Biaya Bunga dan Beban Keuangan Lainnya (Swap)</strong>
            <br>
            Adalah biaya bunga dan beban keuangan lainnya yang dikenakan kepada Nasabah terhadap setiap posisi terbuka dari kontrak yang diperdagangkan dan dapat berubah dari waktu ke waktu.
        </li>

        <li>
            <strong>Margin Requirement</strong>
            <br>
            Adalah sejumlah dana yang dibutuhkan untuk mengambil posisi.
        </li>

        <li>
            <strong>Margin Call</strong>
            <br>
            <ol type="a">
                <li>Nasabah wajib memelihara/memenuhi tingkat Margin yang harus tersedia di rekening sesuai dengan jumlah yang telah ditetapkan baik diminta ataupun tidak oleh perusahaan. Batas waktu penyetoran selambat-lambatnya pada pukul 16:00 WIB pada hari trading bersangkutan.</li>
                <li>Margin Call terjadi apabila Equity ratio Nasabah dibawah 100% dari Margin Requirement atas posisi terbuka serta terjadinya Over Trade. Nasabah diwajibkan menambah sejumlah dana supaya memenuhi Margin yang dibutuhkan. Nasabah dilarang membuka posisi baru sampai kewajiban tersebut terpenuhi, yang mana jika kewajiban tersebut tidak dilaksanakan oleh Nasabah maka perusahaan berhak sepenuhnya untuk melikuidasi posisi terbuka Nasabah baik sebagian atau seluruhnya tanpa perlu persetujuan dari Nasabah terlebih dahulu.</li>
                <li>Pada hari menjelang hari libur pasar, Nasabah wajib memenuhi 100% Margin Requirement (kebutuhan margin atas posisi terbukanya). Jika nasabah tidak memenuhi, maka sesaat setelah waktu penutupan pasar akan dilakukan force-likuidation/likuidasi paksa terhadap sebagian atau seluruh posisi terbukanya tanpa perlu pemberitahuan atau persetujuan dari nasabah terlebih dahulu.</li>
            </ol>
        </li>

        <li>
            <strong>Margin Level (Margin Call Level)</strong>
            <br>
            Adalah perbandingan antara Equity dengan Margin (dalam persen) Margin Level = (Equity/Margin) x 100%.
        </li>

        <li>
            <strong>Free Margin/Available Margin</strong>
            <br>
            Adalah selisih antara Equity dengan Margin <br>
            Equity Margin - Necessary Margin
        </li>

    </ol>


