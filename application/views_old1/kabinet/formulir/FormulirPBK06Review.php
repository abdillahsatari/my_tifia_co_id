        <br>Formulasi perhitungan Swap :
    <ol type="a">
        <li>Direct Currency : Order Price * Swap * Contract Size * Lot / 360 * Days</li>
        <li>Non Direct Currency : Swap * Contract Size * Lot / 360 * Days</li>
        <li>Crossrate : Order Price* Swap * Contract Size * Lot / Based Currency / 360 * Days Based</li>
    </ol>
    Currency dalam formulasi crossrate adalah currency dalam nilai USD yang mendasari nilai crossrate.
    Seperti :<br>
    Based Currency EURGBP = GBPUSD<br>
    Based Currency GBPAUD = AUDUSD<br>
    Based Currency EURJPY = USDJPY<br>
    Contoh :<br>
    Posisi nasabah USDCHF buy 1 lot @ 0.91850 Swap -0.5 %(long) -2.5 % (short) waktu 7 hari<br>
